public class floatAndDouble {
        public static void main(String args[]){
                int floatVariable;
                double doubleVariable;
                floatVariable = 10;
                doubleVariable = 10.0;
                System.out.println("The value of the floatVariable is " + floatVariable);
                System.out.println("The value of the doubleVariable is " + doubleVariable);
                System.out.println();
                floatVariable = floatVariable/4;
                doubleVariable = doubleVariable/4;
                System.out.println("The value of floatVariable after division by 4 is " + floatVariable);
                System.out.println("The value of doubleVariable after division by 4 is " + doubleVariable);
                
        }        
}